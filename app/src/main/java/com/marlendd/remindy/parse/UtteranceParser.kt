package com.marlendd.remindy.parse

/** Результат разбора фразы на предмет и место. */
data class ParsedRecord(val item: String, val location: String)

/**
 * Делит распознанную фразу на предмет и место по первому предлогу.
 *
 * Правило (ТЗ F1): первая часть до предлога – предмет, остальное – место.
 * Место оставляем ВМЕСТЕ с предлогом – «в верхнем ящике стола» читается
 * естественно, в отличие от обрубка «верхнем ящике стола» без предлога.
 *
 * Предлоги матчатся как ЦЕЛЫЕ слова, иначе «зарядка на столе» разделилось бы по
 * «за» внутри слова «зарядка».
 *
 * Глаголы места между предметом и предлогом человек проговаривает естественно, но в
 * названии предмета они лишние – отсекаем хвостовой такой глагол, оставляя чистый предмет.
 * Два вида: состояние/расположение («очки ЛЕЖАТ на столе», «паспорт НАХОДИТСЯ в ящике») и
 * действие «куда я это дел» («паспорт ПОЛОЖУ в комод», «очки УБРАЛ в стол»).
 *
 * Если предлога нет или он стоит первым словом (предмет вышел бы пустым), вся
 * фраза уходит в предмет, место пустое. Предлог без слов после него местом не
 * считается.
 */
object UtteranceParser {

    // «во»/«подо» – озвонченные формы «в»/«под» (Vosk их так и выдаёт: «во втором ящике»)
    private val PREPOSITIONS = setOf("в", "во", "на", "под", "подо", "за")

    // Глаголы места, которые отсекаем из хвоста предмета. Все формы – в ё→е-норме (см. [norm]).
    // Ни одно из слов не является названием предмета, ложных срабатываний нет.
    private val PLACEMENT_VERBS = setOf(
        // Состояние/расположение: «очки ЛЕЖАТ на столе», «паспорт НАХОДИТСЯ в ящике»
        "лежит", "лежат", "лежал", "лежала", "лежало", "лежали",
        "находится", "находятся",
        "стоит", "стоят", "стоял", "стояла", "стояло", "стояли",
        "висит", "висят", "висел", "висела", "висело", "висели",
        "хранится", "хранятся",
        // Причастия: «деньги СПРЯТАНЫ под матрасом», «ключи ОСТАВЛЕНЫ на полке»
        "спрятан", "спрятана", "спрятано", "спрятаны",
        "убран", "убрана", "убрано", "убраны",
        "оставлен", "оставлена", "оставлено", "оставлены",
        "положен", "положена", "положено", "положены",
        "поставлен", "поставлена", "поставлено", "поставлены",
        // Действие «куда я это дел»: «паспорт ПОЛОЖУ в комод», «очки УБРАЛ в стол».
        // 1-е лицо (положу/кладу) + прошедшее (положил/а/о/и) – как человек проговаривает.
        "положу", "положим", "положил", "положила", "положило", "положили",
        "кладу", "кладем", "клал", "клала", "клало", "клали",
        "уберу", "уберем", "убрал", "убрала", "убрало", "убрали",
        "убираю", "убираем",
        "спрячу", "спрячем", "спрятал", "спрятала", "спрятало", "спрятали",
        "прячу", "прячем",
        "поставлю", "поставим", "поставил", "поставила", "поставило", "поставили",
        "ставлю", "ставим", "ставил", "ставила", "ставило", "ставили",
        "повешу", "повесим", "повесил", "повесила", "повесило", "повесили",
        "вешаю", "вешаем", "вешал", "вешала",
        "оставлю", "оставим", "оставил", "оставила", "оставило", "оставили",
        "оставляю", "оставляем",
        "суну", "сунем", "сунул", "сунула", "сунуло", "сунули",
        "засуну", "засунем", "засунул", "засунула", "засунуло", "засунули",
        "сую", "суем",
        "держу", "держим", "держал", "держала", "держало", "держали",
    )

    fun parse(raw: String): ParsedRecord {
        val tokens = raw.trim().split(WHITESPACE).filter { it.isNotEmpty() }
        if (tokens.isEmpty()) return ParsedRecord("", "")

        val prepositionIndex = tokens.indexOfFirst { norm(it) in PREPOSITIONS }
        // -1 (предлога нет) или 0 (предлог первый → предмет пуст) → вся фраза в предмет
        if (prepositionIndex <= 0) {
            return ParsedRecord(stripPlacementVerbs(tokens), "")
        }

        val item = stripPlacementVerbs(tokens.subList(0, prepositionIndex))
        // место = предлог и всё после него; если после предлога пусто – места нет
        val location = if (prepositionIndex < tokens.lastIndex) {
            tokens.subList(prepositionIndex, tokens.size).joinToString(" ")
        } else {
            ""
        }
        return ParsedRecord(item, location)
    }

    // Отбрасывает глаголы места с ОБОИХ концов предмета: русский порядок слов допускает
    // и «паспорт ПОЛОЖИЛ в комод» (хвост), и «ПОЛОЖИЛ паспорт в комод» (начало).
    // Никогда не опустошает предмет (глагол – единственное слово → оставляем как есть).
    private fun stripPlacementVerbs(tokens: List<String>): String {
        var start = 0
        var end = tokens.size
        while (end - start > 1 && norm(tokens[end - 1]) in PLACEMENT_VERBS) end--
        while (end - start > 1 && norm(tokens[start]) in PLACEMENT_VERBS) start++
        return tokens.subList(start, end).joinToString(" ")
    }

    // Нижний регистр + ё→е: Vosk и ручной ввод дают то «кладём», то «кладем» –
    // словари храним в е-норме, сверяем нормализованное слово.
    private fun norm(word: String): String = word.lowercase().replace('ё', 'е')

    private val WHITESPACE = Regex("\\s+")
}
