    /**
     * Строит карту «основа запроса → множество основ, которые она покрывает», прогоняя
     * термины [stemmer]-ом (тем же, что и поиск). Категории линкуют ключ↔член в обе стороны;
     * синонимы – каждую пару внутри группы. Дёшево (несколько сотен связей), зовём один раз.
     */
    fun buildStemmed(stemmer: Stemmer): Map<String, Set<String>> {
        val map = HashMap<String, MutableSet<String>>()
        fun link(a: String, b: String) {
            val sa = stemmer.stem(a)
            val sb = stemmer.stem(b)
            if (sa.isEmpty() || sb.isEmpty() || sa == sb) return
            map.getOrPut(sa) { HashSet() }.add(sb)
        }
        CATEGORIES.forEach { (category, members) ->
            members.forEach { member ->
                link(category, member) // ищем категорию → найдём члена
                link(member, category) // ищем члена → найдём запись с именем категории
            }
        }
        SYNONYMS.forEach { group ->
            for (a in group) for (b in group) if (a != b) link(a, b)
        }
        return map
    }
